#include "fillit.h"

char		**draw_square(char **str, int size)
{
	int		i;
	int		j;

	i = 0;
	while (i < size)
	{
		j = 0;
		while (j < size)
		{
			str[i][j] = '.';
			j++;
		}
		str[i][j] = '\0';
		i++;
	}
	return (str);
}

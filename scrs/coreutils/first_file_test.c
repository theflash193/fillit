#include "fillit.h"

int		first_file_test(char *str)
{
	int i;

	i = 0;
	if (str[0] != '.' && str[0] != '#')
		return (0);
	while (str[i] != '\0')
	{
		if (str[i] != '.' && str[i] != '#' && str[i] != '\n')
			return (0);
		i++;
	}
	return (1);
}

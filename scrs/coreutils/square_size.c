#include "fillit.h"

int		square_size(int *str)
{
	int		i;
	int		k;

	i = 0;
	k = 0;
	while (str[i] != '\0')
		i++;
	k = i * 4;
	i = 0;
	while (i * i < k)
		i++;
	return (i);
}

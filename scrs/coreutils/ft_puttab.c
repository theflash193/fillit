#include "fillit.h"

void	ft_puttab(char **res, t_var var)
{
	int i;
	int j;

	i = 0;
	while (i < var.size)
	{
		j = 0;
		while (res[i][j] != '\0')
			j++;
		ft_putendl(res[i]);
		i++;
	}
}
